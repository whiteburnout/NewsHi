package com.newha.vo;

public class UserScrapNews {
	private String scrapNo;
	private String newsNo;
	private String postNo;
	private String userNo;
	private String date;
	private String curator_summary;
	private String like_cnt;
	private String dislike_cnt;
	
	public UserScrapNews() {}

	public String getScrapNo() {
		return scrapNo;
	}

	public void setScrapNo(String scrapNo) {
		this.scrapNo = scrapNo;
	}

	public String getNewsNo() {
		return newsNo;
	}

	public void setNewsNo(String newsNo) {
		this.newsNo = newsNo;
	}

	public String getPostNo() {
		return postNo;
	}

	public void setPostNo(String postNo) {
		this.postNo = postNo;
	}

	public String getUserNo() {
		return userNo;
	}

	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getCurator_summary() {
		return curator_summary;
	}

	public void setCurator_summary(String curator_summary) {
		this.curator_summary = curator_summary;
	}

	public String getLike_cnt() {
		return like_cnt;
	}

	public void setLike_cnt(String like_cnt) {
		this.like_cnt = like_cnt;
	}

	public String getDislike_cnt() {
		return dislike_cnt;
	}

	public void setDislike_cnt(String dislike_cnt) {
		this.dislike_cnt = dislike_cnt;
	}
	
}
