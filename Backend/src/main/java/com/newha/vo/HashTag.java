package com.newha.vo;

public class HashTag {

	private String tagNo;
	private String name;
	
	public HashTag() {

	}

	public String getTagNo() {
		return tagNo;
	}

	public void setTagNo(String tagNo) {
		this.tagNo = tagNo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
