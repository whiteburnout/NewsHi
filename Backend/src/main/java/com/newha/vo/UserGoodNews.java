package com.newha.vo;

public class UserGoodNews {
	
	private String userNo;
	private String scrapNo;
	private String is_like;
	private String is_dislike;
	private String is_save;
	
	public UserGoodNews() {}

	public String getUserNo() {
		return userNo;
	}

	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}

	public String getScrapNo() {
		return scrapNo;
	}

	public void setScrapNo(String scrapNo) {
		this.scrapNo = scrapNo;
	}

	public String getIs_like() {
		return is_like;
	}

	public void setIs_like(String is_like) {
		this.is_like = is_like;
	}

	public String getIs_dislike() {
		return is_dislike;
	}

	public void setIs_dislike(String is_dislike) {
		this.is_dislike = is_dislike;
	}

	public String getIs_save() {
		return is_save;
	}

	public void setIs_save(String is_save) {
		this.is_save = is_save;
	}
}
